import 'package:flutter/cupertino.dart';

/// this is the checkbox widget style
class CheckBoxWidgetStyle {
  /// decoration
  BorderSide? side;

  /// active color
  Color? activeColor;

  /// check box color
  Color? checkColor;

  /// constructor of the class
  CheckBoxWidgetStyle({
    this.side,
    this.activeColor,
    this.checkColor,
  });
}